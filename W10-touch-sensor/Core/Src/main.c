void HAL_TSC_ConvCpltCallback(TSC_HandleTypeDef* htsc)
{

//	while (HAL_TSC_GetState(htsc) == HAL_TSC_STATE_BUSY) {}
	HAL_TSC_IODischarge(htsc, ENABLE);

	if (HAL_TSC_GroupGetStatus(htsc, TSC_GROUP3_IDX) == TSC_GROUP_COMPLETED) {
		// Store the acquisition value
		uint16_t uhTSCAcquisitionValue = (uint16_t)HAL_TSC_GroupGetValue(htsc, TSC_GROUP3_IDX);

		NewValueFromTSCInterrupt(uhTSCAcquisitionValue);
	}

	if (HAL_TSC_Start_IT(htsc) != HAL_OK) {
		Error_Handler();
	}
}






void NewValueFromTSCInterrupt(uint16_t sample_value) {
	// Serial string turned off as the data is being sent now through the serialiser

		if (mutex == UNLOCKED){
			mutex = LOCKED;
			sprintf(string_to_send, "%hu\r\n", sample_value);
			SerialOutputString(string_to_send, &USART1_PORT);
			if (sample_value < 5200) {
				int16_t new_value = 2000 - (5000 - sample_value)*2;
				if (new_value < 0)
					new_value = 100;
				TIM2->ARR = new_value;
				TIM2->CCR1 = TIM2->ARR / 8;
				//sprintf(string_to_send, "APPLE !!! %hu\r\n", sample_value);
				//SerialOutputString(string_to_send, &USART1_PORT);
			}
			else {
				TIM2->ARR = 10000;
				TIM2->CCR1 = 0;
			}
			mutex = UNLOCKED;
		}

}


